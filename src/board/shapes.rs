pub enum Shape {
    Pulsar,
    PentaDecathlon,
    GliderGun,
}

impl Shape {
    pub const PULSAR: &str = "\
    ..*.*....*.*..
    .............
    *....*..*....*
    *....*..*....*
    *....*..*....*
    ..*.*....*.*..
    .............
    ..*.*....*.*..
    *....*..*....*
    *....*..*....*
    *....*..*....*
    .............
    ..*.*....*.*..
";

    pub const PENTA_DECATHLON: &str = "\
    ..........
    ......*...
    ****..****
    ......*...
    ..........
";

    pub const GLIDER_GUN: &str = "\
........................#............
......................#.#............
............##......##............##.
...........#...#....##............##.
##........#.....#...##...............
##........#...#.##....#.#............
..........#.....#.......#............
...........#...#.....................
............##.......................
";

    pub fn get_shape_string(&self) -> &'static str {
        match self {
            Shape::Pulsar => Shape::PULSAR,
            Shape::PentaDecathlon => Shape::PENTA_DECATHLON,
            Shape::GliderGun => Shape::GLIDER_GUN,
        }
    }
}
